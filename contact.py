class Contact:

    def __init__(self, firstname=None, middlename=None, lastname=None, nickname=None, first_email=None,
                 second_email=None, third_email=None):
        self.firstname = firstname
        self.middlename = middlename
        self.lastname = lastname
        self.nickname = nickname
        self.first_email = first_email
        self.second_email = second_email
        self.third_email = third_email
